export const totalAmounts = invoices.reduce((acc, curr) => {
  Object.keys(curr).forEach((month) => {
    if (
      month !== "invoice" &&
      month !== "memberName" &&
      month !== "post" &&
      month !== "totalShare"
    ) {
      acc[month] = (acc[month] || 0) + curr[month];
    }
  });
  return acc;
}, {});

